    private void methodForSending(){
        String sql = "Insert into TransferMaterialRetailer(Invoice_num,Order_num,Prod_id,Prod_Name,Description,Price,Qty,Status,Vehicle,InvoiceDate,Total_cost)values (?,?,?,?,?,?,?,?,?,?,?)";
         try {
            SimpleDateFormat dateFormate = new SimpleDateFormat("yyyy-MM-dd");
            String date = dateFormate.format(txtdate.getDate());
            pst = con.prepareStatement(sql);
            pst.setString(1, jInvoidNo.getText());
            pst.setString(2, Order_num);
            pst.setString(3, Prod_id);
            pst.setString(4, Prod_Name);
            pst.setString(5, Description);
            pst.setString(6, Price);
            pst.setString(7, Qty);
            pst.setString(8, "Material sended");
            pst.setString(9, jtxtVhcl.getText());
            pst.setString(10, date);
            pst.setString(11, jtxtTotalCost.getText());
            int count = pst.executeUpdate();
            if(0<count){
               sql = "Update RequermentRequest set Status = ? where Prod_id= ? and Order_num = ?";
               pst = con.prepareStatement(sql); 
               pst.setString(1, "Material sended");
               pst.setString(2, Prod_id);
               pst.setString(3, Order_num);
               count=0;
               count = pst.executeUpdate();
               if(0<count){
              sql = "Select Qty,Order_Qty from StoreTable where Prod_id =? and Prod_Name =? ";  
              pst = con.prepareStatement(sql); 
              pst.setString(1, Prod_id);
              pst.setString(2, Prod_Name);
              rs = pst.executeQuery();
              boolean check = rs.next();
              if(check){
                  String dbQty = rs.getString("Qty");
                  String Order_qty = rs.getString("Order_Qty");
                  int dqty = Integer.parseInt(dbQty)-Integer.parseInt(Qty);
                  int orqty = Integer.parseInt(Order_qty)-Integer.parseInt(Qty);
                  if(dbQty.equals(jtxtQty.getText())){
                      sql = "delete from StoreTable where Prod_id= ?";  
                        pst = con.prepareStatement(sql);
                        pst.setString(1, Prod_id);
                        count = 0;
                        count = pst.executeUpdate();
                        if(0<count){
                        JOptionPane.showMessageDialog(this, "Material Transfered Successfully.");
//                        callDeleteProcess(Order_num);
                        System.gc();
                        this.setVisible(false);
                        new Billing(name,id,userType,con).setVisible(true);
                        }else{
                        JOptionPane.showMessageDialog(this, "something went wrong while delete.");
                        }
                  }else{
                        sql = "Update StoreTable set Qty=?,Order_Qty=? where Prod_id= ?";  
                        pst = con.prepareStatement(sql);
                        pst.setString(1, String.valueOf(dqty));
                        pst.setString(2, String.valueOf(orqty));
                        pst.setString(3, Prod_id);
                        count = 0;
                        count = pst.executeUpdate();
                        if(0<count){
                        JOptionPane.showMessageDialog(this, "Material Transfered Successfully.");
                        System.gc();
                        this.setVisible(false);
                        new Billing(name,id,userType,con).setVisible(true);
                        }else{
                            JOptionPane.showMessageDialog(this, "something went wrong while Updating.");
                        }
                  }
              }else{
                 JOptionPane.showMessageDialog(this, "Something went Wrong in StoreTable Getting QTY");   
              }
               }else{
                JOptionPane.showMessageDialog(this, "Something went Wrong\nwhile Transfer Update in RequermentRequest ");   
               }
            }else{
               JOptionPane.showMessageDialog(this, "Something went Wrong");  
            }
            } catch (SQLException ex) {
                Logger.getLogger(InvoiceAndSendMaterial.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
