   private  void autoId(){
        try {
            Statement st = con.createStatement();
            String sql = "select Max(Invoice_num) max from TransferMaterialRetailer";
             rs = st.executeQuery(sql);
             rs.next();            
             rs.getString("max");
             if(rs.getString("max")==null){
                 jInvoidNo.setText("IN001");
             }else{
                 long id = Long.parseLong(rs.getString("max").substring(2, rs.getString("max").length()));
                 id++;
                 jInvoidNo.setText("IN"+String.format("%03d", id));
             }
             
        } catch (SQLException ex) {
            Logger.getLogger(InvoiceAndSendMaterial.class.getName()).log(Level.SEVERE, null, ex);
        }
       
   }
