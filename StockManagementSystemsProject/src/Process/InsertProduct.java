    String Prod_Name= jtxtProductName.getText();
    String Description= jtxtDescription.getText();
    String Costprice= jtxtCostprice.getText();
    String RetailPrice= jtxtRetailPrice.getText();
    String Qty= jtxtQty.getText();
    String vehicleno= jtxtVehicleNo.getText();
    if(!Prod_Id.equals("")&&!Prod_Name.equals("")&&!Description.equals("")&&!Costprice.equals("")&&
            !RetailPrice.equals("")&&!Qty.equals("")&&!vehicleno.equals("")){
         if(allNumCheck()){
        try {
            pst = con.prepareStatement(sql);
            pst.setString(1, Prod_Id);
            pst.setString(2, Prod_Name);
            pst.setString(3, Description);
            pst.setString(4, Costprice);
            pst.setString(5, RetailPrice);
            pst.setString(6, Qty);
            pst.setString(7, vehicleno);
            int count = pst.executeUpdate();
            if(count>0){
                JOptionPane.showMessageDialog(this, "Updated SuccessFully");
                autoId();
                jtxtProductId.setText("");
                jtxtProductName.setText("");
                jtxtDescription.setText("");
                jtxtCostprice.setText("");
                jtxtRetailPrice.setText("");
                jtxtQty.setText("");
                jtxtVehicleNo.setText("");
                jtxtProductId.requestFocus();
            }else{
             JOptionPane.showMessageDialog(this, "something went wrong");   
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(InsertProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
         }   else{
       JOptionPane.showMessageDialog(this, "please enter numbers given below"
               + "\n Costprice"
               + "\n RetailPrice"
               + "\n Qty");
       jtxtCostprice.requestFocus();
    }
    }else{
        JOptionPane.showMessageDialog(this, "Please fill everything");
    }
    
