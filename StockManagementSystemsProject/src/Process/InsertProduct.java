   private  void autoId(){
       String sql = "select Max(tracking_id) max from RequestGRN ";
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            boolean check = rs.next();
            if(check){
                int id = Integer.parseInt(rs.getString("max"));
                ++id;
                jTrackId.setText(String.valueOf(id));
            }else{
               jTrackId.setText("1");
            }
        } catch (SQLException ex) {
            Logger.getLogger(InsertProduct.class.getName()).log(Level.SEVERE, null, ex);
        }
       
   }
