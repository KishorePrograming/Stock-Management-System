    private boolean numcheck(JTextField s){
        boolean flag =false;
        String str = s.getText();
        for(int i=0;i<str.length();i++){
            if('0'<=str.charAt(i)&&str.charAt(i)<='9'){
                flag=true;
            }else{
                flag=false;
                break;
            }
        }
      return flag;  
    }
    private boolean allNumCheck(){
        return numcheck(jtxtCostprice)&&numcheck(jtxtRetailPrice)&&numcheck(jtxtQty);
    }
